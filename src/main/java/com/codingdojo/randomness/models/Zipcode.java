package com.codingdojo.randomness.models;


public class Zipcode {
	
	private Long zipcode;

	
	
	public Zipcode() {
		super();
	}

	public Zipcode(Long zipcode) {
		super();
		this.zipcode = zipcode;
	}

	public Long getZipcode() {
		return zipcode;
	}

	public void setZipcode(Long zipcode) {
		this.zipcode = zipcode;
	}
	
	

}
